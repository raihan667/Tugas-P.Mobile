void main() {
  // Deklarasi Map tanpa inisialisasi langsung
  Map<String, String>? person;

  // Deklarasi Map menggunakan konstruktor
  var product = Map<String, String>();

  // Deklarasi Map menggunakan literal
  var address = <String, String>{};

  print(person);
  print(product);
  print(address);
}
