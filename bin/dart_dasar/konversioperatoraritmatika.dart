void main() {
  var result1 = 10 + 10; // Penjumlahan, hasil: 20

  // var result2 = 100 10; // Salah! Tidak ada operator di antara 100 dan 10
  var result2 = 100 * 10; // Misal ingin perkalian, hasil: 1000

  var result3 = -5; // Negatif 5
  var result4 = 5; // Hati-hati, sebelumnya ada "var result" duplikat
  var result5 = 55; // 55

  var result6 = 10 / 3; // Pembagian biasa, hasil: 3.3333333333333335 (double)
  var result7 = 10 ~/ 3; // Pembagian integer (floor division), hasil: 3

  var result8 = 10 % 3; // Modulus (sisa bagi), hasil: 1

  print(result1);
  print(result2);
  print(result3);
  print(result4);
  print(result5);
  print(result6);
  print(result7);
  print(result8);
}
