void main() {
  // Membuat list bertipe int
  List<int> listInt = [];
  listInt.add(10);
  listInt.add(20);
  listInt.add(30);

  // Membuat list bertipe String
  var listString = <String>[];
  listString.add('Ridho');
  listString.add('Raihan');
  listString.add('Riduan');

  // Menampilkan hasil
  print('listInt: $listInt'); // Output: [10, 20, 30]
  print('listString: $listString'); // Output: [Ridho, Raihan, Riduan]
}
